    private void jButtonSubmitAddBookCopyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSubmitAddBookCopyActionPerformed
        // TODO add your handling code here:
        if (jTextFieldPriceAdd.getText().isEmpty() || jTextFieldNumberofCopyAdd.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Please Fill Data at All Fields !", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            Integer price = Integer.parseInt(jTextFieldPriceAdd.getText());
            Integer numberofcopy = Integer.parseInt(jTextFieldNumberofCopyAdd.getText());
            try {
                new CRUDBookController().addNewBookCopy(booknumber, price, numberofcopy);
            } catch (SQLException ex) {
                Logger.getLogger(AddNewCopy.class.getName()).log(Level.SEVERE, null, ex);
            }
            super.dispose();
            frame.setAlwaysOnTop(true);
            frame.setEnabled(true);
        }
    }//GEN-LAST:event_jButtonSubmitAddBookCopyActionPerformed

