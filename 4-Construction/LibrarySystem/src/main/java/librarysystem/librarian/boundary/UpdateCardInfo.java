    private void jButtonSubmitUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSubmitUpdateActionPerformed
        // TODO add your handling code here:
        if (jTextFieldActivatedCodeUpdate.getText().isEmpty() || jTextFieldExpiredDateUpdate.getText().isEmpty() || jTextFieldUsernameUpdate.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Please Fill Data at All Fields !", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            try {
                new CRUDCardController().updateCard(borrowercardnumber, jTextFieldUsernameUpdate.getText(), Integer.parseInt(jTextFieldActivatedCodeUpdate.getText()), jComboBoxStatusUpdate.getSelectedIndex(), jTextFieldExpiredDateUpdate.getText());
            } catch (SQLException ex) {
                Logger.getLogger(UpdateBookInfo.class.getName()).log(Level.SEVERE, null, ex);
            }
            super.dispose();
            frame.setAlwaysOnTop(true);
            frame.setEnabled(true);
        }
    }//GEN-LAST:event_jButtonSubmitUpdateActionPerformed

